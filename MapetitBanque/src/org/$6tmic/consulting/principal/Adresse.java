package org.$6tmic.consulting.principal;

public class Adresse {
	private String nomDeRue;
	private String complementAdresse;
	private String codePostal;
	private String nomDeVille;
	
	private String numeroDeRue;
	public String getNumeroDeRue() {
		return numeroDeRue;
	}
	public void setNumeroDeRue(String numeroDeRue) {
		this.numeroDeRue = numeroDeRue;
	}
	public String getNomDeRue() {
		return nomDeRue;
	}
	public void setNomDeRue(String nomDeRue) {
		this.nomDeRue = nomDeRue;
	}
	public String getComplementAdresse() {
		return complementAdresse;
	}
	public void setComplementAdresse(String complementAdresse) {
		this.complementAdresse = complementAdresse;
	}
	public String getCodePostal() {
		return codePostal;
	}
	public void setCodePostal(String codePostal) {
		this.codePostal = codePostal;
	}
	public String getNomDeVille() {
		return nomDeVille;
	}
	public void setNomDeVille(String nomDeVille) {
		this.nomDeVille = nomDeVille;
	}
	
}
